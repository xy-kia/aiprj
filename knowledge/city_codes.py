def get_city_code(city: str, platform: str = "boss") -> int | str | None:
    """
    获取指定城市的平台代码

    Args:
        city: 城市名称
        platform: 平台名称 (boss/zhaopin/51job/liepin)

    Returns:
        城市代码或None
    """
    platform_codes = ALL_CITY_CODES.get(platform.lower())
    if not platform_codes:
        return None
    return platform_codes.get(city)


def get_all_cities() -> list[str]:
    """获取所有支持的城市列表"""
    return list(BOSS_CITY_CODES.keys())


def get_core_cities() -> list[str]:
    """获取核心城市列表"""
    return CORE_CITIES.copy()


def is_core_city(city: str) -> bool:
    """判断是否为核心城市"""
    return city in CORE_CITIES


def normalize_city_name(city: str) -> str:
    """
    标准化城市名称
    去除区县后缀，返回市级名称
    """
    # 常见区县后缀
    suffixes = ["区", "县", "市辖区", "新区"]

    for suffix in suffixes:
        if city.endswith(suffix):
            # 特殊处理
            if city in ["浦东新区", "滨海新区"]:
                if city == "浦东新区":
                    return "上海"
                elif city == "滨海新区":
                    return "天津"
            # 一般处理：去除后缀
            city_name = city.replace(suffix, "")
            # 检查是否是有效的城市名
            if city_name in BOSS_CITY_CODES:
                return city_name

    # 处理特殊情况
    city_mapping = {
        "魔都": "上海",
        "帝都": "北京",
        "羊城": "广州",
        "鹏城": "深圳",
        "蓉城": "成都",
        "江城": "武汉",
        "杭城": "杭州",
        "古都": "西安",
        "金陵": "南京",
        "津门": "天津",
    }

    if city in city_mapping:
        return city_mapping[city]

    return city


